using System;

namespace console_dotnet
{
    class Calculadora
    {
        public int calculox(int a, int b) {
            return (a * b);
        }
        public int calculodivisao(int a, int b) {
            return (a / b);
        }
        public int calculosoma(int a, int b) {
            return (a + b);
        }
        public int calculosubtracao(int a, int b) {
            return (a - b);
        }
    }
}